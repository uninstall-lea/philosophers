#include "philosophers.h"

int main(int ac, char **av)
{
	t_data	*data;

	data = _data();
	if (!check_args(ac, av))
		return (FAILURE);
	if (!init_data(ac, av))
		return (FAILURE);
	if (!pthread_create_philo())
		return (FAILURE);
	pthread_mutex_unlock(&(data->mutex.baton_de_parole));
	pthread_join_philo();
	free_all_data();
	return (0);
}
